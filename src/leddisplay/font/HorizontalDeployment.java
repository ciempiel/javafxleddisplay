package leddisplay.font;

/**
 * Enumeration representing a horizontal deployment of character.
 *
 */
public enum HorizontalDeployment {
	/**
	 * A horizontal deployment to the center
	 */
	CENTER,

	/**
	 * A horizontal deployment to the left
	 */
	LEFT,

	/**
	 * A horizontal deployment to the right
	 */
	RIGHT
}
